<div align="center"> 
  <p><strong><font size="5">Aide du moteur de recherche </font></strong></p>
  <p align="left"><font size="4"><strong>Recherche simple :</strong></font></p>
  <p align="left">Vous entrez un ou plusieurs mots dans le champ de recherche 
    et cliquez sur le bouton &quot;rechercher&quot; (ou vous appuyez sur &quot;Entr&eacute;e&quot;).</p>
  <p align="left">Si la recherche porte sur plusieurs termes, le moteur signale 
    uniquement les pages qui comportent <strong>TOUS</strong> ces termes, mais 
    pas n&eacute;cessairement &agrave; la suite les uns des autres.<strong>Pour 
    affiner une recherche, il suffit de sp&eacute;cifier d'autres termes. </strong><br>
    Le moteur ne tient pas compte de la casse, il interpr&egrave;te les lettres 
    composant les termes de recherche comme des <strong>minuscules</strong>.<br>
    Les mots de moins de 3 lettres ne sont pas interpr&eacute;t&eacute;s.</p>
  <p align="left">Le caract&egrave;re <strong>joker</strong> (&quot;*&quot;) peut 
    &ecirc;tre utilis&eacute;. il sert a retrouver des morceaux de mots. Par exemple: 
    gast* retournera les mots gastronomie, gastronome...</p>
  <p align="left">&nbsp;</p>
  <p align="left"><font size="4"><strong>Recherche avanc&eacute;e :</strong></font></p>
  <p align="left">Vous cliquez sur le lien &quot;Recherche avanc&eacute;e&quot; 
    et diff&eacute;rentes options s'affichent:</p>
</div>
<ol>
  <li> 
    <div align="left"><strong>Rechercher dans : Toutes les pages Pages:Fran&ccedil;ais</strong></div>
    Permet de sp&eacute;cifier la langue du document. Seuls les documents appartenant 
    &agrave; la langue choisie seront retourner.</li>
  <li><strong>Rechercher au moins un des mots...</strong> : Le moteur de recherche 
    va renvoyer les documents qui contiennent au moins un mot de la requ&ecirc;te.</li>
  <li><strong>La date</strong> : Seuls les documents ayant une date de derni&egrave;re 
    modification correspondante au crit&egrave;re choisie seront affich&eacute;s.</li>
</ol>
